public class Calculator
{
    public int angka1;
    public int angka2;

    public int Add(int angka1, int angka2){
        return angka1 + angka2;
    }
    public int Mul(int angka1, int angka2){
        return angka1*angka2;
    }
    public int Div(int angka1, int angka2){
        return angka1/angka2;
    }
    public int Sub(int angka1, int angka2){
        return angka1 - angka2;
    }
}
